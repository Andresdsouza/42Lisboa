#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (i * i <= nb && nb <= 2147395600)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}

